def is_leap_year() -> None:
    year: int = int(input('Ingrese un a単o: '))
    
    if (year % 100 == 0) and (year % 400 == 0):
        print(f'El a単o {year} es bisiesto')
    elif year % 4 == 0 and not (year % 100 == 0):
        print(f'El a単o {year} es bisiesto')
    else:
        print(f'El a単o {year} no es bisiesto')
    


